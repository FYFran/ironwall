// ── JS (inline, no external deps) ──

const filterScript = `
function filterFindings() {
  var search = (document.getElementById('search').value || '').toLowerCase();
  var sev = document.getElementById('sevFilter').value;
  var cwe = document.getElementById('cweFilter').value;
  var findings = document.querySelectorAll('.finding');
  var visible = 0;
  findings.forEach(function(f) {
    var title = (f.querySelector('.finding-title').textContent || '').toLowerCase();
    var desc = (f.querySelector('.finding-body').textContent || '').toLowerCase();
    var fsev = f.dataset.severity;
    var fcwe = f.dataset.cwe;
    var match = (sev === 'all' || fsev === sev) &&
                (cwe === 'all' || fcwe === cwe) &&
                (search === '' || title.indexOf(search) >= 0 || desc.indexOf(search) >= 0);
    f.style.display = match ? '' : 'none';
    if (match) visible++;
  });
  document.getElementById('filterCount').textContent = visible + ' / ' + findings.length + ' findings';
}
// Initial count
window.addEventListener('DOMContentLoaded', function() {
  document.getElementById('filterCount').textContent = document.querySelectorAll('.finding').length + ' findings';
});
`
