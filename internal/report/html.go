// WriteHTML generates a self-contained HTML report.
// All user data (code snippets, file paths) is HTML-escaped to prevent XSS.
func WriteHTML(result *ScanResult, cfg *config.Config) error {
	filename := cfg.ReportFilename()
	filename = strings.TrimSuffix(filename, ".md")
	filename = strings.TrimSuffix(filename, ".json")
	filename += ".html"

	f, err := os.Create(filename)
	if err != nil {
		return fmt.Errorf("failed to create HTML report: %w", err)
	}
	defer f.Close()

	w := func(s string, args ...interface{}) {
		fmt.Fprintf(f, s+"\n", args...)
	}

	// ── Header ──
	w(`<!DOCTYPE html>`)
	w(`<html lang="zh-CN">`)
	w(`<head>`)
	w(`<meta charset="UTF-8">`)
	w(`<meta name="viewport" content="width=device-width, initial-scale=1.0">`)
	w(`<title>Ironwall Security Audit — %s</title>`, html.EscapeString(result.Target))
	w(`<style>`)
	w(cssStyles)
	w(`</style>`)
	w(`</head>`)
	w(`<body>`)
	w(`<div class="container">`)

	// ── Header bar ──
	w(`<header>`)
	w(`<h1>🛡️ Ironwall Security Audit</h1>`)
	w(`<div class="meta">`)
	w(`<span>📁 %s</span>`, html.EscapeString(result.Target))
	w(`<span>⏱️ %s</span>`, html.EscapeString(result.Duration))
	w(`<span>📅 %s</span>`, html.EscapeString(result.CompletedAt))
	w(`<span>v%s</span>`, html.EscapeString(result.Version))
	w(`</div>`)
	w(`</header>`)

	// ── Summary cards ──
	s := result.Summary
	w(`<section class="summary">`)
	summaryCard(w, "🔴 Critical", s.Critical, "#dc2626")
	summaryCard(w, "🟠 High", s.High, "#ea580c")
	summaryCard(w, "🟡 Medium", s.Medium, "#ca8a04")
	summaryCard(w, "🟢 Low", s.Low, "#16a34a")
	summaryCard(w, "ℹ️ Info", s.Info, "#6b7280")
	summaryCard(w, "📊 Total", s.Total, "#4f46e5")
	w(`</section>`)

	// AI status
	aiLabel := result.AnalysisStatus
	if aiLabel == "full" {
		aiLabel = "✅ AI analysis complete"
	} else if aiLabel == "partial" {
		aiLabel = "⚠️ AI analysis partial"
	} else if aiLabel == "skipped" {
		aiLabel = "⏭️ AI analysis skipped"
	} else {
		aiLabel = "❌ AI analysis error"
	}
	w(`<div class="ai-status">%s</div>`, html.EscapeString(aiLabel))

	// ── Filter bar ──
	w(`<section class="filters">`)
	w(`<input type="text" id="search" placeholder="🔍 Search findings..." oninput="filterFindings()">`)
	w(`<select id="sevFilter" onchange="filterFindings()">`)
	w(`<option value="all">All Severities</option>`)
	w(`<option value="CRITICAL">🔴 Critical</option>`)
	w(`<option value="HIGH">🟠 High</option>`)
	w(`<option value="MEDIUM">🟡 Medium</option>`)
	w(`<option value="LOW">🟢 Low</option>`)
	w(`<option value="INFO">ℹ️ Info</option>`)
	w(`</select>`)
	w(`<select id="cweFilter" onchange="filterFindings()">`)
	w(`<option value="all">All CWEs</option>`)
	for _, cwe := range uniqueCWEs(result.Findings) {
		w(`<option value="%s">%s</option>`, html.EscapeString(cwe), html.EscapeString(cwe))
	}
	w(`</select>`)
	w(`<span id="filterCount" class="filter-count"></span>`)
	w(`</section>`)

	// ── Findings ──
	w(`<section class="findings" id="findings">`)
	for i, f := range result.Findings {
		renderFinding(w, i, f)
	}
	w(`</section>`)

	// ── Footer ──
	w(`<footer>`)
	w(`<p>Generated by <strong>Ironwall</strong> — AI-powered offline code security audit</p>`)
	w(`<p>⚠️ Findings require human review. Not all detected issues are exploitable.</p>`)
	w(`</footer>`)

	w(`</div>`)

	// ── Scripts ──
	w(`<script>`)
	w(filterScript)
	w(`</script>`)
	w(`</body>`)
	w(`</html>`)

	fmt.Printf("  📄 HTML report saved: %s\n", filename)
	return nil
}

func summaryCard(w func(string, ...interface{}), label string, count int, color string) {
	w(`<div class="card" style="border-left: 4px solid %s">`, color)
	w(`<div class="card-label">%s</div>`, label)
	w(`<div class="card-count">%d</div>`, count)
	w(`</div>`)
}

func renderFinding(w func(string, ...interface{}), idx int, f Finding) {
	sevClass := strings.ToLower(f.Severity.String())
	cwe := f.CWE
	if cwe == "" {
		cwe = "N/A"
	}

	w(`<div class="finding" data-severity="%s" data-cwe="%s" data-id="%s">`,
		html.EscapeString(f.Severity.String()), html.EscapeString(cwe), html.EscapeString(f.ID))

	// Header row
	w(`<div class="finding-header" onclick="this.parentElement.classList.toggle('expanded')">`)
	w(`<span class="sev sev-%s">%s</span>`, sevClass, html.EscapeString(f.Severity.String()))
	w(`<span class="finding-title">%s</span>`, html.EscapeString(f.Title))
	w(`<span class="finding-loc">%s:%d</span>`, html.EscapeString(f.FilePath), f.LineNumber)
	w(`<span class="expand-icon">▼</span>`)
	w(`</div>`)

	// Detail body
	w(`<div class="finding-body">`)

	// Meta grid
	w(`<div class="meta-grid">`)
	metaItem(w, "CWE", cwe)
	metaItem(w, "Category", f.Category)
	metaItem(w, "AI Confidence", fmt.Sprintf("%.0f%%", f.AIConfidence*100))
	metaItem(w, "CVSS", fmt.Sprintf("%.1f", f.CVSS))
	w(`</div>`)

	// Description
	if f.Description != "" {
		w(`<div class="section">`)
		w(`<h3>📝 Description</h3>`)
		w(`<p>%s</p>`, html.EscapeString(f.Description))
		w(`</div>`)
	}

	// Code snippet (XSS SAFE: use textContent via <pre><code>)
	if f.CodeSnippet != "" {
		w(`<div class="section">`)
		w(`<h3>💻 Code</h3>`)
		w(`<pre class="code-block"><code>%s</code></pre>`, html.EscapeString(f.CodeSnippet))
		w(`</div>`)
	}

	// AI Attack Scenario
	if f.AttackScenario != nil {
		w(`<div class="section ai-box">`)
		w(`<h3>🎯 AI Attack Analysis (confidence: %.0f%%)</h3>`, f.AIConfidence*100)
		if f.AttackScenario.IsReal {
			w(`<div class="ai-verdict real">⚠️ Attack scenario is VIABLE</div>`)
		} else {
			w(`<div class="ai-verdict safe">✅ Attack scenario NOT viable</div>`)
		}
		if f.AttackScenario.Actor != "" {
			w(`<h4>👤 Attacker</h4>`)
			w(`<p>%s</p>`, html.EscapeString(f.AttackScenario.Actor))
		}
		if f.AttackScenario.Path != "" {
			w(`<h4>🔗 Attack Path</h4>`)
			w(`<p>%s</p>`, html.EscapeString(f.AttackScenario.Path))
		}
		if f.AttackScenario.Impact != "" {
			w(`<h4>💥 Impact</h4>`)
			w(`<p>%s</p>`, html.EscapeString(f.AttackScenario.Impact))
		}
		if f.AttackScenario.Explanation != "" {
			w(`<h4>📋 Reasoning</h4>`)
			w(`<p>%s</p>`, html.EscapeString(f.AttackScenario.Explanation))
		}
		w(`</div>`)
	}

	// Fix suggestion
	if f.FixSuggestion != "" {
		w(`<div class="section">`)
		w(`<h3>🔧 Fix</h3>`)
		w(`<p>%s</p>`, html.EscapeString(f.FixSuggestion))
		w(`</div>`)
	}

	w(`</div>`) // finding-body
	w(`</div>`) // finding
}

func metaItem(w func(string, ...interface{}), label, value string) {
	w(`<div class="meta-item"><strong>%s</strong><br>%s</div>`, html.EscapeString(label), html.EscapeString(value))
}

func uniqueCWEs(findings []Finding) []string {
	seen := map[string]bool{}
	var result []string
	for _, f := range findings {
		cwe := f.CWE
		if cwe == "" {
			cwe = "N/A"
		}
		if !seen[cwe] {
			seen[cwe] = true
			result = append(result, cwe)
		}
	}
	return result
}

